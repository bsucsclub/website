<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=5 STYLE="font-size: 20pt">Computer
Science Club</FONT></P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=5 STYLE="font-size: 20pt">1-30-08</FONT></P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=5 STYLE="font-size: 20pt">Article
I – Name</FONT></P>
<P STYLE="margin-bottom: 0in">The name of this organization chartered
under this constitution shall be the Bemidji State University
Computer Science Club.</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=5 STYLE="font-size: 20pt">Article
II – Purpose</FONT></P>
<P STYLE="margin-bottom: 0in">The purpose of this organization will
be to stimulate interest in and foster the goals of the Computer
Science Club.</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=5 STYLE="font-size: 20pt">Article
III – Membership</FONT></P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
I	Memberships shall be open to all Bemidji State University Students
and interested parties. Membership shall consist of a minimum of 25%
Bemidji State students.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
II	Membership in this organization will not be discriminatory with
regard to race, color, creed, sex, sexual orientation, religion,
marital status, national origin, or status with regard to public
assistance or physical disability.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-bottom: 0in">Section III	Members must adhere to the
constitution in order to retain membership.</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
IV	Members will be required to regularly attend meetings in order to
retain membership status.</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=5 STYLE="font-size: 20pt">Article
IV – Finances</FONT></P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
I	There will be no dues required of anyone wishing to gain or retain
membership.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
II	In the event that the organization should dissolve, all remaining
funds shall be donated to the Bemidji State University Foundation.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
III	In the event that the organization should dissolve, any equipment
and remaining funds obtained by the club shall be donated to the
Computer Science department.</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=5 STYLE="font-size: 20pt">Article
V – Officers</FONT></P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
I	The officers of this organization shall be a President,
Vice-President, Secretary, and Treasurer. Together, along with any
committee chairs, they shall comprise the Executive Board.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"></P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
II	The powers of the Executive Board shall be as follows:</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<OL TYPE=a>
	<LI><P STYLE="margin-bottom: 0in">The power to establish and
	dissolve committees, also known as project groups.</P>
</OL>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<OL TYPE=a START=2>
	<LI><P STYLE="margin-bottom: 0in">The power to schedule meetings.</P>
</OL>
<P STYLE="margin-left: 0.5in; margin-bottom: 0in"><BR>
</P>
<OL TYPE=a START=3>
	<LI><P STYLE="margin-bottom: 0in">The power to regulate membership
	according to the conditions in Article III.</P>
</OL>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-bottom: 0in">Section II	The duties of these officers
shall be as follows:</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<OL TYPE=a>
	<LI><P STYLE="margin-bottom: 0in">The President shall preside and
	maintain order at meetings, facilitate debate and motions, enforce
	the rules of this constitution, take care of other officer’s
	duties in their absence, and other duties that may arise.</P>
</OL>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<OL TYPE=a START=2>
	<LI><P STYLE="margin-bottom: 0in">The Vice-President will assist the
	President in his/her duties and preside over the organization in
	his/her absence.</P>
</OL>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<OL TYPE=a START=3>
	<LI><P STYLE="margin-bottom: 0in">The Secretary shall be in charge
	of all correspondence of the organization and other secretarial
	duties.</P>
</OL>
<P STYLE="margin-left: 0.5in; margin-bottom: 0in"><BR>
</P>
<OL TYPE=a START=4>
	<LI><P STYLE="margin-bottom: 0in">The Treasurer will handle
	paperwork dealing with finances, including: paying bills, and
	tracking and reporting revenue/expenditures.</P>
</OL>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
III	All officers shall be elected by secret ballot three months
before the end of spring semester. The elected officials will be
trained under the current officials for the remaining three months
and will assume full duties at the beginning of the next fall
semester.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
IV	Should an officer’s position not be filled, either through
resignation or other form of leaving, the position shall be filled
for the remainder of the term by a member chosen by a majority vote
of the remaining Executive Board.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
V	Should there be no officers in place, the remaining members shall
elect new officers for the remainder of the terms by secret ballot.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
VI	The term for each office will be one academic year.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
VII	At least one member of the Executive Board must be a BSU student.</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=5 STYLE="font-size: 20pt">Article
VI – Organizational Structure</FONT></P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
I	Meetings shall be scheduled weekly with at least one meeting per
semester to remain active.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"></P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
II	All members shall have one vote with a simple majority rule of a
quorum of the membership. Quorum shall be half plus one members.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
III	Special meetings shall be held in accordance to the Executive
Board’s decision and must give members a minimum of 48 hours
notice.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P ALIGN=CENTER STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">
<FONT SIZE=5 STYLE="font-size: 20pt">Article VII - Committees</FONT></P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
I	The Executive Board shall establish committees, from now on
referred to as project groups, to achieve a specific task. If
established, the project group chair shall become part of the
Executive Board.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
II	Upon establishment, the Executive Board shall decide the project
group’s name, purpose, and chair. All other project group members
shall be chosen by the chair.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
III	Upon completion of its purpose, the Executive Board shall
dissolve the project group.</P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in"><BR>
</P>
<P ALIGN=CENTER STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">
<FONT SIZE=5 STYLE="font-size: 20pt">Article VIII – Parliamentary
Authority</FONT></P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
I	Meetings shall be held in accordance to <U>Robert’s Rules of
Order, Newly Revised</U>.</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=5 STYLE="font-size: 20pt">Article
IX – Advisor</FONT></P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
I	An advisor will be selected from the Bemidji State University
faculty and staff who will support and participate in the
organization’s activities.</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=5 STYLE="font-size: 20pt">Article
X – Ratification</FONT></P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
I	This constitution shall be, upon completion, sent to the Student
Senate. After approval by the Bemidji State University Student Senate
and by the Associate Director of Hobson Memorial Union, it will be
sent to the University President or his/her designee, for
ratification.</P>
<P STYLE="margin-bottom: 0in"><BR>
</P>
<P ALIGN=CENTER STYLE="margin-bottom: 0in"><FONT SIZE=5 STYLE="font-size: 20pt">Article
XI – Amendments</FONT></P>
<P STYLE="margin-left: 1in; text-indent: -1in; margin-bottom: 0in">Section
I	This constitution may be amended by a two-thirds membership vote at
any meeting. The proposed amendment must have been submitted to the
Secretary for a reading at the previous meeting. This amendment must
then be sent to the Bemidji State University Student Senate for
ratification.</P>
<P STYLE="margin-bottom: 0in"><BR>
